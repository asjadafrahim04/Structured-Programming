#include<stdio.h>
int main(){
int x=50;
  printf("You entered an integer: %d\n",x);
float y=6.12345;
  printf("You entered a float number : %0.3f\n",y);
char ch='s';
  printf("You entered a character: %c\n", ch);
double z=2.34;
  printf("You entered a double number : %lf\n",z);
return 0;
}

